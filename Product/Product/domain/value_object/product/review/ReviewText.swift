import Foundation
import Core

public struct ReviewText: Equatable {
    
    private var _value: String;
    
    private init(value: String) {
        self._value = value;
    }
    
    public static func create(with name: String) -> ResultOption<ReviewText, AppError> {
        let validName = Guard.againstNilValue(argument: name)
        if !validName {
            return .error(AppError.nilValueNotAllowed)
        }
        return .ok(ReviewText(value: name))
    }
    
    public var text: String {
           return self._value
    }
    
}
